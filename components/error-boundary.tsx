'use client'

import { Component, type ReactNode } from 'react'
import { ApiError, NetworkError } from '@/lib/api/errors'

interface Props {
  children: ReactNode
  fallback?: (error: Error, reset: () => void) => ReactNode
}

interface State {
  hasError: boolean
  error: Error | null
}

export class ErrorBoundary extends Component<Props, State> {
  constructor(props: Props) {
    super(props)
    this.state = { hasError: false, error: null }
  }

  static getDerivedStateFromError(error: Error): State {
    return { hasError: true, error }
  }

  reset = () => {
    this.setState({ hasError: false, error: null })
  }

  render() {
    if (this.state.hasError && this.state.error) {
      if (this.props.fallback) {
        return this.props.fallback(this.state.error, this.reset)
      }

      const isNetworkError = this.state.error instanceof NetworkError
      const isApiError = this.state.error instanceof ApiError
      const status = isApiError ? (this.state.error as ApiError).status : null

      return (
        <div className="flex flex-col items-center justify-center min-h-[200px] p-8 text-center">
          <div className="text-destructive mb-4">
            {isNetworkError ? (
              <NetworkIcon className="h-12 w-12" />
            ) : status === 404 ? (
              <NotFoundIcon className="h-12 w-12" />
            ) : (
              <ErrorIcon className="h-12 w-12" />
            )}
          </div>
          <h2 className="text-xl font-semibold mb-2">
            {isNetworkError
              ? 'Connection Error'
              : status === 404
                ? 'Not Found'
                : 'Something went wrong'}
          </h2>
          <p className="text-muted-foreground mb-4 max-w-md">
            {isNetworkError
              ? 'Please check your internet connection and try again.'
              : status === 404
                ? 'The requested resource could not be found.'
                : this.state.error.message || 'An unexpected error occurred.'}
          </p>
          <button
            onClick={this.reset}
            className="px-4 py-2 bg-primary text-primary-foreground rounded-md hover:bg-primary/90 transition-colors"
          >
            Try again
          </button>
        </div>
      )
    }

    return this.props.children
  }
}

function ErrorIcon({ className }: { className?: string }) {
  return (
    <svg
      className={className}
      fill="none"
      viewBox="0 0 24 24"
      stroke="currentColor"
      strokeWidth={1.5}
    >
      <path
        strokeLinecap="round"
        strokeLinejoin="round"
        d="M12 9v3.75m9-.75a9 9 0 11-18 0 9 9 0 0118 0zm-9 3.75h.008v.008H12v-.008z"
      />
    </svg>
  )
}

